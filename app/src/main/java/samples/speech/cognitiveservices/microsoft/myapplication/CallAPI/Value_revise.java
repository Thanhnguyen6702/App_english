public class Value_revise {
    private String Tienganh;
    private String Tiengviet;
    private String Day_revise;

    public Value_revise(String tienganh, String tiengviet) {
        Tienganh = tienganh;
        Tiengviet = tiengviet;
    }

    public String getTienganh() {
        return Tienganh;
    }

    public void setTienganh(String tienganh) {
        Tienganh = tienganh;
    }

    public String getTiengviet() {
        return Tiengviet;
    }

    public void setTiengviet(String tiengviet) {
        Tiengviet = tiengviet;
    }

    public String getDay_revise() {
        return Day_revise;
    }

    public void setDay_revise(String day_revise) {
        Day_revise = day_revise;
    }
}
